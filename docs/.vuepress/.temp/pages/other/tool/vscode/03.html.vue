<h2 id="_3-2-方法二" tabindex="-1"><a class="header-anchor" href="#_3-2-方法二"><span>3.2 方法二</span></a></h2>
<p>备注：如果遇见使用方法一离线安装失败的情况，建议使用该方法进行安装，如 vetur 插件下载不成功，可以使用此方法。</p>
<p>操作步骤：</p>
<ol>
<li>
<p>查找已安装的插件列表文件：路径一般为 C:\Users\用户名.vscode\extensions</p>
</li>
<li>
<p>将所需使用的插件文件复制到U盘，用U盘拷贝相关文件到需要离线安装的电脑上的 .vscode/extensions 文件夹下即可，重启vscode 即可安装成功。</p>
</li>
</ol>
