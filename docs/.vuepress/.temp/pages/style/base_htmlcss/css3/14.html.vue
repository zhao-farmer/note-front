<template><div><h1 id="十四、多列布局" tabindex="-1"><a class="header-anchor" href="#十四、多列布局"><span>十四、多列布局</span></a></h1>
<p>常用属性如下：</p>
<ul>
<li>
<p>给容器添加</p>
<ul>
<li>column-count ：指定列数，值是数字。</li>
<li>column-width ：指定列宽，值是长度。</li>
<li>columns ：同时指定列宽和列数，复合属性；值没有数量和顺序要求。</li>
<li>column-gap ：设置列边距，值是长度。</li>
<li>column-rule-style ：设置列与列之间边框的风格，值与 border-style 一致。</li>
<li>column-rule-width ：设置列与列之间边框的宽度，值是长度。</li>
<li>column-rule-color ：设置列与列之间边框的颜色。</li>
<li>coumn-rule ：设置列边框，复合属性。</li>
</ul>
</li>
<li>
<p>给具体的标签属性添加</p>
<ul>
<li>column-span 指定是否跨列；值: none 、 all 。</li>
</ul>
</li>
</ul>
</div></template>


